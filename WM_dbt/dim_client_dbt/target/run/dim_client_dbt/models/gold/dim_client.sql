
  
    

        create or replace transient table SNOWFLAKE_RND.gold.dim_client
         as
        (SELECT


MD5(
    CLIENT_ID || CAST(DBT_VALID_FROM AS STRING)
) AS CLIENT_SK,

CLIENT_ID,
CLIENT_FULL_NAME,
CLIENT_FIRST_NAME,
CLIENT_GENDER,
CLIENT_DATE_OF_BRITH,
CLIENT_DEFAULT_EMAIL,
CLIENT_DEFAULT_MOBILE,
CLIENT_COUNTRY_OF_RESIDENCE,
CLIENT_COMPANY_NAME,
CLIENT_COMPANY_TYPE,

DBT_VALID_FROM AS EFFECTIVE_START_DATE,

COALESCE(
    DBT_VALID_TO,
    '9999-12-31'::TIMESTAMP_NTZ
) AS EFFECTIVE_END_DATE,

CASE
    WHEN DBT_VALID_TO IS NULL THEN 'Y'
    ELSE 'N'
END AS IS_CURRENT_FLAG,
CDC_TIMESTAMP


FROM SNOWFLAKE_RND.gold.dim_client_snapshot
        );
      
  