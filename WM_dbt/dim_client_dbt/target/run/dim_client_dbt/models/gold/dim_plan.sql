
  
    

        create or replace transient table SNOWFLAKE_RND.gold.dim_plan
         as
        (SELECT


MD5(
    plan_id || CAST(DBT_VALID_FROM AS STRING)
) AS plan_sk,
PLAN_ID
, PLAN_CATEGORY_NAME
, PLAN_CLIENT_CATEGORY
, PLAN_END_DATE
, PLAN_POLICY_NUMBER
, PLAN_PRODUCT_TYPE
, PLAN_PROVIDER_NAME
, PLAN_SELLING_ADVISER_FULL_NAME
, PLAN_SERVICING_ADVISER_FULL_NAME
, PLAN_START_DATE
, PLAN_STATUS
, DBT_VALID_FROM AS EFFECTIVE_START_DATE,

COALESCE(
    DBT_VALID_TO,
    '9999-12-31'::TIMESTAMP_NTZ
) AS EFFECTIVE_END_DATE,

CASE
    WHEN DBT_VALID_TO IS NULL THEN 'Y'
    ELSE 'N'
END AS IS_CURRENT_FLAG,
CDC_TIMESTAMP


FROM SNOWFLAKE_RND.gold.dim_plan_snapshot
        );
      
  