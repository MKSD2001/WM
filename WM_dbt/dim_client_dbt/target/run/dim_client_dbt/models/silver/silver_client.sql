
  
    

        create or replace transient table SNOWFLAKE_RND.silver.silver_client
         as
        (WITH client_dedup AS (

SELECT
    CLIENT_ID,
    CLIENT_FULL_NAME,
    CLIENT_FIRST_NAME,
    CLIENT_GENDER,
    CLIENT_DATE_OF_BRITH,
    CLIENT_DEFAULT_EMAIL,
    CLIENT_DEFAULT_MOBILE,
    CLIENT_COUNTRY_OF_RESIDENCE,
    CLIENT_COMPANY_NAME,
    CLIENT_COMPANY_TYPE,
    CDC_ACTION,
    CDC_TIMESTAMP,

    ROW_NUMBER() OVER (
        PARTITION BY CLIENT_ID
        ORDER BY CDC_TIMESTAMP DESC
    ) AS RN

FROM SNOWFLAKE_RND.ODS.CLIENT_CDC WHERE CDC_ACTION <> 'DELETE'
)

SELECT
CLIENT_ID,
CLIENT_FULL_NAME,
CLIENT_FIRST_NAME,
CLIENT_GENDER,
CLIENT_DATE_OF_BRITH,
CLIENT_DEFAULT_EMAIL,
CLIENT_DEFAULT_MOBILE,
CLIENT_COUNTRY_OF_RESIDENCE,
CLIENT_COMPANY_NAME,
CLIENT_COMPANY_TYPE,
CDC_ACTION,
CDC_TIMESTAMP,
MD5(
    COALESCE(CLIENT_FULL_NAME,'') ||
    COALESCE(CLIENT_FIRST_NAME,'') ||
    COALESCE(CLIENT_GENDER,'') ||
    COALESCE(CLIENT_DEFAULT_EMAIL,'') ||
    COALESCE(CLIENT_DEFAULT_MOBILE,'') ||
    COALESCE(CLIENT_COUNTRY_OF_RESIDENCE,'') ||
    COALESCE(CLIENT_COMPANY_NAME,'') ||
    COALESCE(CLIENT_COMPANY_TYPE,'')
) AS ROW_HASH

FROM client_dedup
WHERE RN = 1
        );
      
  