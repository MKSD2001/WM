
  
    

        create or replace transient table SNOWFLAKE_RND.silver.silver_plan
         as
        (WITH client_dedup AS (

SELECT
      PLAN_ID
, PLAN_CATEGORY_NAME
, PLAN_CLIENT_CATEGORY
, PLAN_END_DATE
, PLAN_POLICY_NUMBER
, PLAN_PRODUCT_TYPE
, PLAN_PROVIDER_NAME
, PLAN_SELLING_ADVISER_FULL_NAME
, PLAN_SERVICING_ADVISER_FULL_NAME
, PLAN_START_DATE
, PLAN_STATUS
, CDC_ACTION
, CDC_TIMESTAMP

    ,ROW_NUMBER() OVER (
        PARTITION BY PLAN_ID
        ORDER BY CDC_TIMESTAMP DESC
    ) AS RN

FROM SNOWFLAKE_RND.ODS.PLAN_CDC WHERE CDC_ACTION <> 'DELETE'
)

SELECT
  PLAN_ID
, PLAN_CATEGORY_NAME
, PLAN_CLIENT_CATEGORY
, PLAN_END_DATE
, PLAN_POLICY_NUMBER
, PLAN_PRODUCT_TYPE
, PLAN_PROVIDER_NAME
, PLAN_SELLING_ADVISER_FULL_NAME
, PLAN_SERVICING_ADVISER_FULL_NAME
, PLAN_START_DATE
, PLAN_STATUS
, CDC_ACTION
, CDC_TIMESTAMP

,MD5(
     COALESCE(PLAN_CATEGORY_NAME,'')||
    COALESCE(PLAN_CLIENT_CATEGORY,'')||
    COALESCE(PLAN_END_DATE,'9999-12-31'::DATE)||
    COALESCE(PLAN_POLICY_NUMBER,'')||
    COALESCE(PLAN_PRODUCT_TYPE,'')||
    COALESCE(PLAN_PROVIDER_NAME,'')||
    COALESCE(PLAN_SELLING_ADVISER_FULL_NAME,'')||
    COALESCE(PLAN_SERVICING_ADVISER_FULL_NAME,'')||
    COALESCE(PLAN_START_DATE,'1900-01-01'::DATE)||
    COALESCE(PLAN_STATUS,'')
) AS ROW_HASH

FROM client_dedup
WHERE RN = 1
        );
      
  